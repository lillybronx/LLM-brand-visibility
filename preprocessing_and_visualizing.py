if 'intent_label' not in brands_data.columns:
    def extract_core_intent(job_id_string):
        if pd.isna(job_id_string):
            return 'unknown_intent'
        parts = str(job_id_string).split('::')
        if len(parts) > 1 and parts[1]:
            if re.match(r'^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$', parts[0]):
                return parts[1]
            else:
                return parts[0]
        elif len(parts) == 1:
            if re.match(r'^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$', parts[0]):
                return 'unknown_intent'
            else:
                return parts[0]
        return 'unknown_intent'
    brands_data['intent_label'] = brands_data['job_id'].apply(extract_core_intent)

# Define the list of intent labels to compare
comparison_intent_labels = ['best_affordable', 'recommend', 'most_popular', 'best_quality', 'best_brands_uk']

# Filter data for the specified intent types
filtered_data_for_stack = brands_data[
    brands_data['intent_label'].isin(comparison_intent_labels)
].copy()

# Explode the brand_names to get individual brands and clean up
