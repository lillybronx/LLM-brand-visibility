pivoted_data_stacked = pivoted_data_stacked.sort_values(by='total', ascending=False).drop(columns='total')

# Reorder columns to ensure consistent display (optional, but good practice)
pivoted_data_stacked = pivoted_data_stacked[top_n_overall_brands]

# Plotting the stacked bar chart
plt.figure(figsize=(16, 9))
